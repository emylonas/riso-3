            <h3>Mediating the Risorgimento / Risorgimento Mediato</h3>
						<p style="text-align:justify;padding-right:15px;">The international symposium Mediating the Risorgimento / Risorgimento mediato was held at Brown University on April 14 and 15, 2011, to mark  the occasion of  the 150th-anniversary of united Italy. The topic of the symposium was the role of old and new media (so defined within the framework of their time) in the production and dissemination of an “imagined Italian community” and the articulation of a national discourse, against the backdrop of 19th-century Europe.  </p>
<p style="text-align:justify;padding-right:15px;">Scholars from Italy, the UK and the US were invited to address the question of how different media (illustrated news, opera and theatre, painting and photography, panoramas and film) contributed to shaping the social and political relationships and strategies that resulted in Italian unification, gave voice and expression to national narratives and iconographies, provided a bridge between elite and popular culture, and influenced post-unitarian debates. A selection of the papers presented at the symposium are collected in the Journal of Modern Italian Studies, volume 18, issue 2 (2013), edited by John Davis (University of Connecticut at Storrs) and Massimo Riva (Brown University) and published by Taylor & Francis.</p>
<p style="text-align:justify;padding-right:15px;">The symposium consisted of four sessions: “Risorgimento in Print,” chaired by John Davis; “Visual Cultures of the Risorgimento,” chaired by Emily Braun (CUNY); “Performing the Risorgimento,” chaired by Suzanne Stewart-Steinberg (Brown); and “Cinema and pre-cinema of the Risorgimento,” chaired by Massimo Riva. A round-table discussion, “Mediating the Risorgimento: Past and Present,” chaired by David Kertzer (Brown) concluded the event.</p>

