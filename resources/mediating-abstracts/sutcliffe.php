<h5>Marketing ‘Garibaldi Panoramas’ in Britain (1860-1864)</h5>
<p style="text-align:justify">
This paper maps the UK itineraries of Garibaldi panoramas from the liberation of the South in 1860, to Garibaldi's visit to London, in 1864. Garibaldi panoramas' initial fortunes stemmed from the marketing impresarios' ability to harness the potential of a form of spectacle which drew its content subject from the accounts of the extraordinary events unrolling in Italy, reported in the press. The paper shows that while the popularity of the genre reached its peak in early 1861, this rapidly waned, despite the marketing efforts sustained by travelling panorama impresarios. In linking the decline of this entertainment genre with the spread of the negative press surrounding the newly-established Italian parliament, this study contends that the decrease in popularity of the Garibaldi panoramas  amongst British audiences found root in the growing, prosaic press accounts on Italy which  came to undermine the newsworthiness of these spectacles. </p>

<h5>Il mercato dei panorami garibaldini in Gran Bretagna (1860-1864)</h5>
<p style="text-align:justify">
Questo studio traccia gli itinerari dei panorami garibaldini in Gran Bretagna dalla liberazione del sud, nel 1860, alla visita di Garibaldi a Londra, nel 1864. La fortuna iniziale dei panorami garibaldini ebbe origine nell'abilità strategica degli impresari di cogliere il potenziale di una formula vincente, combinare l'intrattenimento dello spettacolo con la pretensione di informare il pubblico sugli eventi straordinari svoltisi in Italia. Lo studio dimostra che la popolarità di questo genere raggiunse il suo culmine nel 1861, scemando tuttavia rapidamente, nonostante gli sforzi degli impresari itineranti. Collegando il declino di questo genere di intrattenimento con la diffusione di una stampa negativa sul governo italiano da poco costituito, questo studio sostiene che il declino in popolarità  dei panorami garibaldini ebbe origine nella proliferazione di prosaici resoconti gionalistici che andarono a minare una importante attrattiva di tali spettacoli, la loro attualità.</p>

