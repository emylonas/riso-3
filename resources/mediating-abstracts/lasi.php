<h5><em>La presa di Roma</em> and <em>Il piccolo garibaldino</em>: The Risorgimento and National Identity in Early Italian Cinema </h5>
<p style="text-align:justify">
The first film to be industrially produced in Italy, <em>La presa di Roma</em> (Alberini & Santoni, 1905), marks the beginning of a relationship between Italian cinema and the Risorgimento that will have an enduring and fruitful future. Between 1905 and 1927 over sixty films will be based on the Risorgimento, each one realized in its own particular style and with its own historical interpretation. This diversity of cinematic approaches to representing the Risorgimento is already visible in two films made just 4 years apart, <em>La presa di Roma</em> (1905) and <em>Il piccolo garibaldino</em> (1909). Through a reconstruction of the historical studies conducted during the restoration of the two films, this essay analyzes and compares the essential characteristics of each film relating their choices of imagery and style to the historical and political context in which they were made.</p>

<h5><em>La presa di Roma</em> e <em>Il piccolo garibaldino</em>: Risorgimento e identità nazionale nel cinema italiano delle origini</h5>
<p style="text-align:justify">
Il primo film prodotto industrialmente in Italia è <em>La presa di Roma</em> (Alberini & Santoni, 1905): il cinema italiano nasce dunque “risorgimentale” e il Risorgimento sarà uno dei soggetti privilegiati nella storia della cinematografia nazionale. All’epoca del cinema muto si realizzano in Italia, tra il 1905 e il 1927, una sessantina di film dedicati alla vicenda risorgimentale che viene trasposta sullo schermo secondo le più differenti impostazioni estetiche e stilistiche e le più svariate interpretazioni storiche. Una diversità di prospettiva che già è visibile confrontando due film prodotti a soli 4 anni di distanza: <em>La presa di Roma</em> del 1905 e <em>Il piccolo garibaldino</em> realizzato nel 1909. Ripercorrendo gli studi storici effettuati in occasione della collaborazione al restauro di entrambi, il mio contributo si propone di analizzare le caratteristiche essenziali dei due film, evidenziandone le differenze dal punto di vista figurativo, rappresentativo, culturale e politico.</p>

