<h5>How political were Verdi’s operas?  Metaphors of progress in the reception of <em>I Lombardi alla prima crociata </em>
</h5>
<p style="text-align:justify">
This article re-examines claims for liberal and patriotic significance traditionally made for the operas Verdi composed for La Scala in the early 1840s.  Although it is still commonly asserted that Verdi’s music mobilized a new kind of national consciousness, the journalistic reception of <em>Nabucco</em> (1842) and <em>I Lombardi alla prima crociata</em> (1843) give few hints that critics were interested in the images of national identity, unity, or colonial oppression alluded to in those works.  This silence is all the more striking in contrast with contemporary writing on Francesco Hayez, where critics found ways to comment on the national themes explored in the canvasses, despite censorship.   Departing from this surprising divergence between music and art criticism, the article examines reviews of<em> I Lombard</em>i as a case study for how we might extract historical information from a documentary record that was so deeply shaped by censorship and self-censorship.</p>

